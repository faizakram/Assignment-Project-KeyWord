package com.assignment.web.dto;

public class CommonDTO {

	private Long id;
	private String name;
	
	public CommonDTO() {
	}

	public CommonDTO(Long id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
